package potager.btalva.mng;


public class InputDataPotagerBernardException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public InputDataPotagerBernardException() {
		super();
		
	}

	public InputDataPotagerBernardException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public InputDataPotagerBernardException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public InputDataPotagerBernardException(String message) {
		super(message);
		
	}

	public InputDataPotagerBernardException(Throwable cause) {
		super(cause);
		
	}
	
	

}